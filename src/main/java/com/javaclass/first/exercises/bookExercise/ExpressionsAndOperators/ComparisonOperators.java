package com.javaclass.first.exercises.bookExercise.ExpressionsAndOperators;

public class ComparisonOperators {
    public static void main(String[] args) {
        int g = 5;
        int h = 6;
        if (g == h)
            System.out.println("g equals h");
        if (g != h)
            System.out.println("g not equals h");
        if (g < h)
            System.out.println("g less thain h");
        if (g <= h)
            System.out.println("g less thain or equals h");
        if (g > h)
            System.out.println("g greater thain h");
        if (g >= h)
            System.out.println("g greater thain or equals h");
    }
}
