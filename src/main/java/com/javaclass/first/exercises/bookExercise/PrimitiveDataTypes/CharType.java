package com.javaclass.first.exercises.bookExercise.PrimitiveDataTypes;

public class CharType {
    public static void main(String args[]) {
        char a1 = 'a';
        System.out.println("Char a1= " + a1);

        char aleph = '\u05D0';
        System.out.println(aleph);

        char a2 = 'J';
        char a3 = 'o';
        char a4 = 'h';
        char a5 = 'n';
        System.out.println("Char name = " + a2 + a3 + a4 + a5);

        String lastName = "Smith";
        System.out.println("String last name = " + lastName);


    }

}
